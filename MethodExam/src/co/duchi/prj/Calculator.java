package co.duchi.prj;

public class Calculator {
	public int sum(int a, int b) {
		return a + b;
	}

	public void sub(int a, int b) {
		System.out.println(a - b);
	}

	public void sub(float f, int b) {
		System.out.println(f - b);
	}

	public long sub(long l, long m) {
		return l - m;
	}
}
